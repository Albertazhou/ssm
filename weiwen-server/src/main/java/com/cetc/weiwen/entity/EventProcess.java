package com.cetc.weiwen.entity;

public class EventProcess {

	private Integer eventId;
	
	private Integer record;
	
	private Integer face;
	
	private Integer involved;
	
	private Integer message;
	
	private Integer sign;
		
	private Integer prosecute;
	
	private Integer education;
	
	private Integer possession;
	
	private Integer reply;
	
	private Integer finished;

	public Integer getEventId() {
		return eventId;
	}

	public void setEventId(Integer eventId) {
		this.eventId = eventId;
	}

	public Integer getRecord() {
		return record;
	}

	public void setRecord(Integer record) {
		this.record = record;
	}

	public Integer getFace() {
		return face;
	}

	public void setFace(Integer face) {
		this.face = face;
	}

	public Integer getInvolved() {
		return involved;
	}

	public void setInvolved(Integer involved) {
		this.involved = involved;
	}

	public Integer getMessage() {
		return message;
	}

	public void setMessage(Integer message) {
		this.message = message;
	}

	public Integer getSign() {
		return sign;
	}

	public void setSign(Integer sign) {
		this.sign = sign;
	}

	public Integer getProsecute() {
		return prosecute;
	}

	public void setProsecute(Integer prosecute) {
		this.prosecute = prosecute;
	}

	public Integer getFinished() {
		return finished;
	}

	public void setFinished(Integer finished) {
		this.finished = finished;
	}

	public Integer getEducation() {
		return education;
	}

	public void setEducation(Integer education) {
		this.education = education;
	}

	public Integer getPossession() {
		return possession;
	}

	public void setPossession(Integer possession) {
		this.possession = possession;
	}

	public Integer getReply() {
		return reply;
	}

	public void setReply(Integer reply) {
		this.reply = reply;
	}
	
	
}
