package com.cetc.weiwen.vo;


import com.cetc.weiwen.entity.PeopleNum;

/**
 * EventLevelContrast的视图对象方便查询
 * @author liqiaofei
 *
 */
public class EventLevelContrastVo {
	private Integer id;

	private EventTypeVo eventTypeVo;

	private PeopleNum peopleNum;

	private Integer level;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public EventTypeVo getEventTypeVo() {
		return eventTypeVo;
	}

	public void setEventTypeVo(EventTypeVo eventTypeVo) {
		this.eventTypeVo = eventTypeVo;
	}

	public PeopleNum getPeopleNum() {
		return peopleNum;
	}

	public void setPeopleNum(PeopleNum peopleNum) {
		this.peopleNum = peopleNum;
	}

	public Integer getLevel() {
		return level;
	}

	public void setLevel(Integer level) {
		this.level = level;
	}

	@Override
	public String toString() {
		return "EventLevelContrastVo [id=" + id + ", eventTypeVo=" + eventTypeVo + ", peopleNum=" + peopleNum + ", level="
				+ level + "]";
	}
	
	
}
