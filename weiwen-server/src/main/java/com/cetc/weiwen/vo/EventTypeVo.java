package com.cetc.weiwen.vo;

import com.cetc.weiwen.entity.EventType;

public class EventTypeVo {

	private Integer id;

	private String typeName;

	private EventType pEventType;


	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public EventType getpEventType() {
		return pEventType;
	}

	public void setpEventType(EventType pEventType) {
		this.pEventType = pEventType;
	}

	@Override
	public String toString() {
		return "EventTypeVo [id=" + id + ", typeName=" + typeName + ", pEventType=" + pEventType + "]";
	}
	
}
