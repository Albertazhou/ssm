package com.cetc.weiwen.vo;

import java.util.Date;

import com.cetc.weiwen.entity.User;

public class SmsInfoVo {

	private Integer id;

    private User user;

    private String civicsIdcard;

    private Integer taskId;

    private Integer eventId;

    private Integer content;

    private Date createTime;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public String getCivicsIdcard() {
		return civicsIdcard;
	}

	public void setCivicsIdcard(String civicsIdcard) {
		this.civicsIdcard = civicsIdcard;
	}

	public Integer getTaskId() {
		return taskId;
	}

	public void setTaskId(Integer taskId) {
		this.taskId = taskId;
	}

	public Integer getEventId() {
		return eventId;
	}

	public void setEventId(Integer eventId) {
		this.eventId = eventId;
	}

	public Integer getContent() {
		return content;
	}

	public void setContent(Integer content) {
		this.content = content;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
    
    
}
